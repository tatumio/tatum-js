export type BtcTx = {
    /**
     * Transaction hash.
     */
    hash?: string;
    /**
     * Witness hash in case of a SegWit transaction.
     */
    witnessHash?: string;
    /**
     * Fee paid for this transaction, in satoshis.
     */
    fee?: number;
    rate?: number;
    mtime?: number;
    /**
     * Block this transaction belongs to.
     */
    blockNumber?: number;
    /**
     * Time of the transaction.
     */
    time?: number;
    /**
     * Index of the transaction in the block.
     */
    index?: number;
    /**
     * Index of the transaction.
     */
    version?: number;
    /**
     * List of transactions, from which assets are being sent.
     */
    inputs?: Array<{
        prevout?: {
            /**
             * Transaction hash of the input.
             */
            hash?: string;
            /**
             * Transaction index of the input.
             */
            index?: number;
        };
        /**
         * Data generated by a spender which is almost always used as variables to satisfy a pubkey script.
         */
        script?: string;
        /**
         * Transaction witness.
         */
        witness?: string;
        sequence?: number;
        coin?: {
            version?: number;
            height?: number;
            /**
             * Amount of the transaction, in Satoshis (1 BTC = 100 000 000 Satoshis)
             */
            value?: number;
            script?: string;
            /**
             * Sender address.
             */
            address?: string;
            /**
             * Coinbase transaction - miner fee.
             */
            coinbase?: boolean;
        };
    }>;
    /**
     * List of recipient addresses and amounts to send to each of them.
     */
    outputs?: Array<{
        /**
         * Sent amount in satoshis.
         */
        value?: number;
        /**
         * Transaction script.
         */
        script?: string;
        /**
         * Recipient address.
         */
        address?: string;
    }>;
    /**
     * Block this transaction was included in.
     */
    locktime?: number;
}
